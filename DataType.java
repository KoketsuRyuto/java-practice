class DataType {
  public static void main(String[] args) {
    int month = 12;
    int day = 29;
    float weight = 58.0f;
    double height = 171.5;
    char bloodType = 'B';

    String name = "纐纈";

    System.out.println("こんにちは！" + name + "です");
    System.out.println("身長は" + height + "cm、体重は" + weight + "kg、");
    System.out.println("誕生日は" + month + "月" + day + "日、");
    System.out.println("血液型は" + bloodType + "型です。");
  }
}
